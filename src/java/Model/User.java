package Model;

/**
 *
 * @author ACER
 */
public class User {
    private String acc, pass, name, address, dob;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public User(String account, String password) {
        this.acc = account;
        this.pass = password;
    }

    public User(String account, String password, String name, String address, String dob) {
        this.acc = account;
        this.pass = password;
        this.name = name;
        this.address = address;
        this.dob = dob;
    }

    public String getAcc() {
        return acc;
    }

    public void setAcc(String acc) {
        this.acc = acc;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
    
}
